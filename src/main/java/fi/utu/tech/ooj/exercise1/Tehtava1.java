package fi.utu.tech.ooj.exercise1;

public class Tehtava1 {
    /** Tehtävä 1
     *
     * Metodi area lukee syötteenä kaksi muuttujaa ja palauttaa niiden tulon
     *
     * @.pre (w >= 0 && != null) (h >= 0 && != null)
     * @.post RESULT >= 0
     */
    public static double area(double w, double h) {
        return w * h;
    }
}
